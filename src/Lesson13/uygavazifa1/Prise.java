package Lesson13.uygavazifa1;

public class Prise {
    private final double massa;
    private final double sum;

    public Prise(double massa, double sum) {
        this.massa = massa;
        this.sum = sum;
    }

    public double getMassa() {
        return massa;
    }

    public double getSum() {
        return sum;
    }

}
