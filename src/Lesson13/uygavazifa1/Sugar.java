public class Sugar {
    private double sugar;
    private double sum;

    public Sugar(double sugar, double sum) {
        this.sugar = sugar;
        this.sum = sum;
    }



    public double getSugar() {
        return sugar;
    }

    public double getSum() {
        return sum;
    }

    public void setSum(double sum) {
        this.sum = sum;
    }
    public Sugar(){

    }

    public void setSugar(double sugar) {
        this.sugar = sugar;
    }
}
